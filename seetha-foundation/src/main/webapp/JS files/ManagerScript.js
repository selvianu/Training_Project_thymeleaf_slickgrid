
//open stock table popup on the screen
function openStock() {
	var stockTable = document.getElementById("stock-table-id");
	var donationTable = document.getElementById("donation-status-table");
	var civilianTable = document.getElementById("civilians-table");
	var volunteersTable = document.getElementById("volunteers-table");
	var distributorTable = document.getElementById("distributor-table");
	stockTable.style.display = "block";
	donationTable.style.display = "none";
	civilianTable.style.display = "none";
	volunteersTable.style.display = "none";
	distributorTable.style.display = "none";
}


